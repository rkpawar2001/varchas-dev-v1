/* varchas-product.js */

document.addEventListener('DOMContentLoaded', function() {
  
  // 1. Variant Selection Logic
  const variantInput = document.getElementById('pdpVariantId');
  const priceDisplay = document.getElementById('pdpPrice');
  const comparePriceDisplay = document.getElementById('pdpComparePrice');
  const atcBtn = document.getElementById('atcBtn');
  const stockBarWrap = document.querySelector('.stock-bar-wrap');
  const stockBar = document.querySelector('.stock-bar');
  const stockText = document.querySelector('.stock-text');
  
  // productVariants is defined in the liquid template
  const variants = window.productVariants || [];
  
  let currentOptions = {};
  
  // Initialize currentOptions from active selections generated by liquid
  document.querySelectorAll('.pdp-option').forEach(optionEl => {
    const key = optionEl.dataset.optionName;
    const activeEl = optionEl.querySelector('.active');
    if (activeEl) {
      currentOptions[key] = activeEl.dataset.value;
    }
  });

  function updateVariant() {
    // Find matching variant
    const matchedVariant = variants.find(v => {
      // Check if all selected options match the variant's options
      return Object.keys(currentOptions).every((key, index) => {
        // Variant option keys are option1, option2, option3
        const optionKey = `option${index + 1}`;
        return v[optionKey] === currentOptions[key];
      });
    });

    if (matchedVariant) {
      // Update hidden input
      if (variantInput) variantInput.value = matchedVariant.id;
      
      // Update price
      if (priceDisplay && matchedVariant.price) {
        priceDisplay.textContent = formatMoney(matchedVariant.price);
      }
      
      if (comparePriceDisplay) {
        if (matchedVariant.compare_at_price > matchedVariant.price) {
          comparePriceDisplay.textContent = formatMoney(matchedVariant.compare_at_price);
          comparePriceDisplay.style.display = 'block';
          const discountEl = document.querySelector('.pdp-discount');
          if(discountEl) {
            discountEl.textContent = 'SAVE ' + formatMoney(matchedVariant.compare_at_price - matchedVariant.price);
            discountEl.style.display = 'inline-block';
          }
        } else {
          comparePriceDisplay.style.display = 'none';
          const discountEl = document.querySelector('.pdp-discount');
          if(discountEl) discountEl.style.display = 'none';
        }
      }

      // Update button state
      if (matchedVariant.available) {
        atcBtn.disabled = false;
        atcBtn.innerHTML = '<svg width="18" height="18" viewBox="0 0 24 24" fill="none" class="atc-icn" stroke="currentColor" stroke-width="2.5" stroke-linecap="round" stroke-linejoin="round"><path d="M6 2L3 6v14a2 2 0 002 2h14a2 2 0 002-2V6l-3-4z"/><line x1="3" y1="6" x2="21" y2="6"/><path d="M16 10a4 4 0 01-8 0"/></svg> ADD TO CART';
        
        // Update stock bar dummy visual based on inventory_quantity if available, else static
        if(matchedVariant.inventory_quantity > 0 && matchedVariant.inventory_quantity < 10) {
           if(stockText) stockText.textContent = `⚡ Only ${matchedVariant.inventory_quantity} left inside`;
           if(stockBarWrap) stockBarWrap.style.display = 'block';
           if(stockBar) stockBar.style.width = '20%';
        } else {
           if(stockText) stockText.textContent = `SELLING FAST`;
           if(stockBarWrap) stockBarWrap.style.display = 'block';
           if(stockBar) stockBar.style.width = '60%';
        }
      } else {
        atcBtn.disabled = true;
        atcBtn.textContent = 'SOLD OUT';
        if(stockBarWrap) stockBarWrap.style.display = 'none';
        if(stockText) stockText.textContent = 'RESTOCKING SOON';
      }

      // If the variant has a featured image, update the main image
      // Assuming thumbnails have data-media-id
      if (matchedVariant.featured_media) {
        const thumbBtn = document.querySelector(`.thumb[data-media-id="${matchedVariant.featured_media.id}"]`);
        if (thumbBtn) thumbBtn.click();
      }

    } else {
      // Variant doesn't exist (impossible combination)
      atcBtn.disabled = true;
      atcBtn.textContent = 'UNAVAILABLE';
    }
  }

  function formatMoney(cents) {
    if (!cents) return '₹ 0';
    return '₹ ' + (cents / 100).toLocaleString('en-IN');
  }

  // Bind Color Clicks
  window.selectColor = function(el, optionName, value) {
    const container = el.closest('.color-opts');
    container.querySelectorAll('.color-opt').forEach(opt => opt.classList.remove('active'));
    el.classList.add('active');
    
    // Update label
    const labelSpan = document.getElementById(`opt-label-${optionName}`);
    if (labelSpan) labelSpan.textContent = value;
    
    currentOptions[optionName] = value;
    updateVariant();
  };

  // Bind Size Clicks
  window.selectSize = function(el, optionName, value) {
    // If it's pure sold-out block, maybe skip (optional, based on your CSS)
    if(el.classList.contains('sold-out')) return;

    const container = el.closest('.size-opts');
    container.querySelectorAll('.size-opt').forEach(opt => opt.classList.remove('active'));
    el.classList.add('active');
    
    // Update label
    const labelSpan = document.getElementById(`opt-label-${optionName}`);
    if (labelSpan) labelSpan.textContent = value;
    
    currentOptions[optionName] = value;
    updateVariant();
  };

  // Quantity Controls
  window.changeQty = function(d) {
    const qtyInput = document.getElementById('pdpQty');
    const qtyDisplay = document.getElementById('qtyVal');
    if (!qtyInput || !qtyDisplay) return;
    
    let currentQty = parseInt(qtyInput.value) || 1;
    let newQty = Math.max(1, currentQty + d);
    qtyInput.value = newQty;
    qtyDisplay.textContent = newQty;
  };

  // Form Submit Intercept (to work with AJAX Cart)
  const pdpForm = document.getElementById('pdpForm');
  if (pdpForm) {
    pdpForm.addEventListener('submit', function(e) {
      e.preventDefault();
      
      const formData = new FormData(pdpForm);
      const variantId = formData.get('id');
      const quantity = formData.get('quantity') || 1;
      
      if (!variantId) return;

      if (window.VARCHAS_CART && typeof window.VARCHAS_CART.submitForm === 'function') {
        window.VARCHAS_CART.submitForm(pdpForm, atcBtn);
      } else {
        // Fallback standard submit
        pdpForm.submit();
      }
    });
  }

  // 2. UI Interactions (Tabs, Accordion, Gallery)

  window.switchThumb = function(el, mediaUrl) {
    document.querySelectorAll('.thumb').forEach(t => t.classList.remove('active'));
    el.classList.add('active');
    
    const mainImg = document.getElementById('mainProdImg');
    const gmArt = document.querySelector('.gm-product-art');
    if (mainImg) {
       mainImg.src = mediaUrl;
       mainImg.style.display = 'block';
       if(gmArt) gmArt.style.display = 'none';
    }
  };

  window.toggleAcc = function(btn) {
    const body = btn.nextElementSibling;
    const isOpen = btn.classList.contains('open');
    btn.classList.toggle('open', !isOpen);
    body.classList.toggle('open', !isOpen);
  };

  window.openTab = function(id) {
    document.querySelectorAll('.tab-btn').forEach(b => {
      b.classList.toggle('active', b.dataset.tabTarget === id);
    });
    document.querySelectorAll('.tab-pane').forEach(p => {
      p.classList.toggle('active', p.id === 'tab-' + id);
    });
    if(id !== 'story') {
      const tabsSec = document.querySelector('.pdp-tabs-section');
      if(tabsSec) tabsSec.scrollIntoView({behavior:'smooth', block:'start'});
    }
  };

  // Initial update
  if(variants.length > 0) {
      updateVariant();
  }

  // 3. Countdown Timer (Visual Mockup for Urgency)
  const endTime = Date.now() + ((8 * 3600 + 43 * 60 + 22) * 1000);
  function updateTimer() {
    const diff = Math.max(0, Math.floor((endTime - Date.now()) / 1000));
    const h = Math.floor(diff / 3600);
    const m = Math.floor((diff % 3600) / 60);
    const s = diff % 60;
    
    const hEl = document.getElementById('cdH');
    const mEl = document.getElementById('cdM');
    const sEl = document.getElementById('cdS');
    
    if (hEl) hEl.textContent = (h < 10 ? '0' : '') + h;
    if (mEl) mEl.textContent = (m < 10 ? '0' : '') + m;
    if (sEl) sEl.textContent = (s < 10 ? '0' : '') + s;
  }
  updateTimer();
  setInterval(updateTimer, 1000);

  // 4. Sticky ATC
  const stickyAtc = document.getElementById('stickyAtc');
  if (stickyAtc && atcBtn) {
    if ('IntersectionObserver' in window) {
      const io = new IntersectionObserver(entries => {
        stickyAtc.style.transform = entries[0].isIntersecting ? 'translateY(100%)' : 'translateY(0)';
      }, { threshold: 0.1 });
      io.observe(atcBtn);
    }
  }

});
